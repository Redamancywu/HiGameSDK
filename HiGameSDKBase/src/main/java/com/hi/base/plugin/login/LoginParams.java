package com.hi.base.plugin.login;

public class LoginParams {
    //登录参数
    public String account;
    public String password;

    public String token;

    public String deviceId;

    public String deviceType;

    public String deviceName;

    public String deviceModel;

    public String deviceOs;

    public String deviceOsVersion;

    public String deviceManufacturer;

    public String deviceManufacturerModel;

    public String deviceManufacturerOs;

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getDeviceId() {
        return deviceId;
    }

    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }

    public String getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(String deviceType) {
        this.deviceType = deviceType;
    }

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    public String getDeviceModel() {
        return deviceModel;
    }

    public void setDeviceModel(String deviceModel) {
        this.deviceModel = deviceModel;
    }

    public String getDeviceOs() {
        return deviceOs;
    }

    public void setDeviceOs(String deviceOs) {
        this.deviceOs = deviceOs;
    }

    public String getDeviceOsVersion() {
        return deviceOsVersion;
    }

    public void setDeviceOsVersion(String deviceOsVersion) {
        this.deviceOsVersion = deviceOsVersion;
    }

    public String getDeviceManufacturer() {
        return deviceManufacturer;
    }

    public void setDeviceManufacturer(String deviceManufacturer) {
        this.deviceManufacturer = deviceManufacturer;
    }

    public String getDeviceManufacturerModel() {
        return deviceManufacturerModel;
    }

    public void setDeviceManufacturerModel(String deviceManufacturerModel) {
        this.deviceManufacturerModel = deviceManufacturerModel;
    }

    public String getDeviceManufacturerOs() {
        return deviceManufacturerOs;
    }

    public void setDeviceManufacturerOs(String deviceManufacturerOs) {
        this.deviceManufacturerOs = deviceManufacturerOs;
    }
}
